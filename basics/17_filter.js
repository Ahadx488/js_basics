//   ===`forEach()`=== is used only to iterate over an array and perform actions on each element, but it does **not return a new array**, so assigning its result to a variable gives `undefined`. In contrast, ===`filter()`=== is used to **select elements based on a condition** and always returns a **new array** containing only the elements that satisfy the condition. This makes `filter()` ideal for extracting subsets of data, such as numbers greater than a value or objects matching multiple criteria (e.g., books of a specific genre published after a certain year). While the same filtering logic can be implemented using `forEach()` with manual pushing into a new array, `filter()` is cleaner, more readable, and purpose-built for this task.
